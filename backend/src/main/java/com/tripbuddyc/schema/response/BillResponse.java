package com.tripbuddyc.schema.response;

public class BillResponse {
    private Integer id;

    private Integer sum;

    public BillResponse() {

    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSum() {
        return sum;
    }

    public void setSum(Integer sum) {
        this.sum = sum;
    }
}
